package com.yuki.base;
/**
 * 关系运算符
 */
public class Demo16 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 21;

        //模运算
        System.out.println(c%a); //1
        System.out.println(a>b); //false
        System.out.println(a<b); //true
        System.out.println(a==b); //false
        System.out.println(a!=b); //true
    }
    
}
