package com.yuki.base;
/*
整数拓展：进制
**/
public class Demo02 {
    public static void main(String[] args) {
        int i = 10;
        int i2 = 010; //八进制0
        int i3 = 0x10; //十六进制0x

        System.out.println(i); //10
        System.out.println(i2); //8
        System.out.println(i3); //16
    }
    
}
